def new_id() -> str:
    return uuid.uuid4().hex


def types_compatible(output_type: str, input_type: str) -> bool:
    # Exec pins must match exec pins only
    if output_type == "exec" or input_type == "exec":
        return output_type == "exec" and input_type == "exec"

    # "any" matches anything (non-exec)
    if output_type == "any" or input_type == "any":
        return True

    return output_type == input_type


def sanitize_identifier(name: str, fallback: str = "x") -> str:
    s = (name or "").strip()
    if not s:
        return fallback
    s = re.sub(r"[^0-9a-zA-Z_]", "_", s)
    if re.match(r"^[0-9]", s):
        s = "_" + s
    return s if s.isidentifier() else fallback


@dataclass
class PinData:
    pin_id: str
    name: str
    pin_type: str
    is_output: bool


@dataclass
class NodeData:
    node_id: str
    kind: str
    title: str
    x: float
    y: float
    inputs: List[PinData]
    outputs: List[PinData]
    props: Dict[str, Any] = field(default_factory=dict)


@dataclass
class ConnectionData:
    out_pin_id: str
    in_pin_id: str


@dataclass
class GraphWarning:
    node_id: str
    code: str
    message: str
    pin_id: Optional[str] = None


class CodeWriter:
    def __init__(self) -> None:
        self.lines: List[str] = []
        self.indent: int = 0

    def push(self) -> None:
        self.indent += 4

    def pop(self) -> None:
        self.indent = max(0, self.indent - 4)

    def write(self, line: str = "") -> None:
        pad = " " * self.indent
        self.lines.append(pad + line if line else "")


